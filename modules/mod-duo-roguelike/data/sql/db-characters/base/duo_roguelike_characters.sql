CREATE TABLE IF NOT EXISTS `duo_rl_account_progress` (
  `account_id` INT UNSIGNED NOT NULL,
  `essence` INT UNSIGNED NOT NULL DEFAULT 0,
  `total_runs` INT UNSIGNED NOT NULL DEFAULT 0,
  `best_level` TINYINT UNSIGNED NOT NULL DEFAULT 1,
  `total_boss_kills` INT UNSIGNED NOT NULL DEFAULT 0,
  `total_deaths` INT UNSIGNED NOT NULL DEFAULT 0,
  `created_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  `updated_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (`account_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS `duo_rl_character_state` (
  `guid` INT UNSIGNED NOT NULL,
  `account_id` INT UNSIGNED NOT NULL,
  `run_id` INT UNSIGNED NOT NULL DEFAULT 1,
  `origin_race` TINYINT UNSIGNED DEFAULT 0,
  `origin_name` VARCHAR(32) DEFAULT NULL,
  `hardcore_dead` TINYINT UNSIGNED NOT NULL DEFAULT 0,
  `death_level` TINYINT UNSIGNED DEFAULT NULL,
  `death_map` INT UNSIGNED DEFAULT NULL,
  `death_zone` INT UNSIGNED DEFAULT NULL,
  `death_area` INT UNSIGNED DEFAULT NULL,
  `created_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  `updated_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (`guid`),
  KEY `idx_duo_rl_character_state_account` (`account_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS `duo_rl_unlocks` (
  `account_id` INT UNSIGNED NOT NULL,
  `unlock_key` VARCHAR(64) NOT NULL,
  `unlocked_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (`account_id`, `unlock_key`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS `duo_rl_character_perks` (
  `guid` INT UNSIGNED NOT NULL,
  `perk_key` VARCHAR(64) NOT NULL,
  `rank` TINYINT UNSIGNED NOT NULL DEFAULT 1,
  `learned_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (`guid`, `perk_key`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;
